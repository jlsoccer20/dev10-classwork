public class ExampleOperand {

    public static void main(String[] args){
        int operand1 = 12;
        int operand2 = 35;
        int total = sum(operand1, operand2);
        System.out.println(total);
    }

    public static int sum(int operand1, int operand2){
        operand1 = 0;
        operand2 = 250;
        return operand1 + operand2;
    }
}
